<?php

// The example of for loop
for($i = 0; $i <5; $i++) {
    echo "Perulangan ke-$i<br>";
}

// The example of while loop
$i = 1;
while($i < 15) {
    echo "Naruto mengeluarkan kagebunshin sebanyak $i kali<br>";
    $i++;
}

// The example of do while loop
$i = 1;
do {
    echo "Sehari saya makan $i kali<br>";
    $i++;
} while($i <= 3);